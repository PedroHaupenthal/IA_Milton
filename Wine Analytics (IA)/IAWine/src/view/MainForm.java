    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        
        if(VerificarCampos()){
            try {
                GravarArqVinho();
            
                new CheckFile(lblStatus, ProgressBar, lblQualidadeVinho).start();
            
                if(rbBranco.isSelected()){
                    lblTipoVinho.setText("Branco");
             
                    Runtime.getRuntime().exec("cmd /c start "+FileLocation.EXEC+
                                        " /k \"cd "+FileLocation.LOCAL_ARCHIVE+
                                        " && "+FileLocation.ARCHIVE_NAME_WHITE+"\"");
                }
                else{
                    lblTipoVinho.setText("Vermelho");
                    Runtime.getRuntime().exec("cmd /c start "+FileLocation.EXEC+
                                        " /k \"cd "+FileLocation.LOCAL_ARCHIVE+
                                        " && "+FileLocation.ARCHIVE_NAME_RED+"\"");
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ocorreu um erro ao tentar analisar o vinho");
            }
        }
        
    }//GEN-LAST:event_btnIniciarActionPerformed

