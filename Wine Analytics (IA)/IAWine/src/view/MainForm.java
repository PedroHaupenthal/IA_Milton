    public void DeleteArchives(){
        File output;
        //wine
        output = new File(FileLocation.LOCAL_OUTPUTS+"\\"+FileLocation.OUTPUT_WINE);
        if(output.exists()){
            output.delete();
        }
        //quality
        output = new File(FileLocation.LOCAL_OUTPUTS+"\\"+FileLocation.OUTPUT_QLT);
        if(output.exists()){
            output.delete();
        }
    }
    
