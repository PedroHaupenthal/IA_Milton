    private void GravarArqVinho() throws IOException{
        
        FileWriter arq = new FileWriter(constants.FileLocation.LOCAL_OUTPUTS+"\\"+constants.FileLocation.OUTPUT_WINE);
        PrintWriter gravarArq = new PrintWriter(arq);

        gravarArq.printf("%s;%s;%s;%s;%s;%s;%s;%s;%s;%s;%s", txtAcidezFixada.getText(), txtAcidezVolatil.getText(), txtAcidezCitrica.getText(),
                                    txtAcucar.getText(), txtCloretos.getText(), txtDioEnxofreLivre.getText(),
                                    txtDioEnxofreTotal.getText(), txtDensidade.getText(), txtPh.getText(),
                                    txtSulfatos.getText(), txtAlcool.getText());

        arq.close();
    }
    
    private boolean VerificarCampos(){
        if(txtAcidezCitrica.getText().equals("") || txtAcidezFixada.getText().equals("") ||
            txtAcidezVolatil.getText().equals("") || txtDioEnxofreLivre.getText().equals("") ||
            txtAcucar.getText().equals("") || txtAcucar.getText().equals("") ||
            txtAlcool.getText().equals("") || txtCloretos.getText().equals("") ||
            txtDensidade.getText().equals("") || txtDioEnxofreTotal.getText().equals("") ||
            txtPh.getText().equals("") || txtSulfatos.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Todos as propriedades do vinho devem ser preenchidas.");
            return false;
        }
        else{
            return true;
        }
    }

    private void MascaraNumeros(KeyEvent evt) {
        String caracteres="0987654321.";
        if(!caracteres.contains(evt.getKeyChar()+"")){
            evt.consume();
        }
    }
